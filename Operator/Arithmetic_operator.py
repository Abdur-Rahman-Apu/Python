
a=1
b=2
#Additon(+)
print(f"Addition of {a}+{b}={a+b}")
#Subtraction(-)
print(f"Subtraction of {a}-{b}={a-b}")
#Multiplication(*)
print(f"Multiplication of {a}*{b}={a*b}")
#Division and floor division
print(f"Division of {a}/{b}={a/b} and floor division={a//b}")
#modulus operator
print(f"Remainder of {a}%{b}={a%b}")
#x to the power y -> x**y
print(f"{a} to the power of {b} = {a**b}")